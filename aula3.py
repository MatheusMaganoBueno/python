"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos  que estão dentro de aspas
"""

# Aspas simples 
print('Matheus Bueno')
print(1,'Matheus "Bueno"')

# Aspas duplas 
print("Matheus Bueno")
print(2, "Matheus 'Bueno'")

# Escape
print("Matheus \"Bueno\"")

# r
print(r"Matheus \"Bueno\"")